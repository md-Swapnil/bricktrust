export function getProjectBySlug(slug: string) {
  return projects.find((p) => p.slug === slug);
}

export function getProjectsByCategory(category: string) {
  return projects.filter((p) => p.category === category);
}

export function getProjectsByCity(citySlug: string) {
  return projects.filter((p) => p.citySlug === citySlug);
}

export function getProjectsByDeveloper(developerSlug: string) {
  return projects.filter((p) => p.developerSlug === developerSlug);
}

export function getRelatedProjects(slug: string, category: string, limit = 3) {
  return projects
    .filter((p) => p.slug !== slug && p.category === category)
    .concat(projects.filter((p) => p.slug !== slug && p.category !== category))
    .slice(0, limit);
}

export function overallScore(score: Project["score"]) {
  const values = Object.values(score);
  const avg = values.reduce((a, b) => a + b, 0) / values.length;
  return Math.round(avg * 10) / 10;
}
